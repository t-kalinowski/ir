use std::env;
use std::fs;
use std::path::PathBuf;

#[path = "src/rig/release_metadata.rs"]
mod release_metadata;

const R_VERSION_METADATA_FETCHED_AT_PATH: &str = "src/rig/r-versions-fetched-at.txt";
const R_VERSION_METADATA_PATH: &str = "src/rig/r-versions.json";

fn main() {
    println!("cargo:rerun-if-changed=build.rs");
    println!("cargo:rerun-if-changed=src/rig/release_metadata.rs");
    println!("cargo:rerun-if-changed={R_VERSION_METADATA_FETCHED_AT_PATH}");
    println!("cargo:rerun-if-changed={R_VERSION_METADATA_PATH}");

    let json = fs::read_to_string(R_VERSION_METADATA_PATH)
        .unwrap_or_else(|e| panic!("failed to read `{R_VERSION_METADATA_PATH}`: {e}"));
    let releases = release_metadata::parse_release_metadata_json(&json, R_VERSION_METADATA_PATH)
        .unwrap_or_else(|e| panic!("{e}"));
    let fetched_at = fs::read_to_string(R_VERSION_METADATA_FETCHED_AT_PATH)
        .unwrap_or_else(|e| panic!("failed to read `{R_VERSION_METADATA_FETCHED_AT_PATH}`: {e}"));
    let fetched_at = parse_metadata_date(&fetched_at, R_VERSION_METADATA_FETCHED_AT_PATH)
        .unwrap_or_else(|e| panic!("{e}"));
    let out_dir = PathBuf::from(env::var_os("OUT_DIR").expect("OUT_DIR is set by Cargo"));
    fs::write(
        out_dir.join("r_version_releases.rs"),
        generated_release_metadata(&releases, &fetched_at),
    )
    .expect("failed to write generated R release metadata");
}

fn generated_release_metadata(
    releases: &[release_metadata::RMinorReleaseMetadata],
    fetched_at: &str,
) -> String {
    let mut output = String::from("// @generated by build.rs\n");
    output.push_str("const EMBEDDED_MINOR_RELEASES_FETCHED_AT: &str = ");
    output.push_str(&format!("{fetched_at:?};\n"));
    output.push_str("const EMBEDDED_MINOR_RELEASES: &[RMinorRelease<'static>] = &[\n");
    for release in releases {
        output.push_str("    RMinorRelease {\n");
        output.push_str("        major: ");
        output.push_str(&release.major.to_string());
        output.push_str(",\n");
        output.push_str("        minor: ");
        output.push_str(&release.minor.to_string());
        output.push_str(",\n");
        output.push_str("        date: ");
        output.push_str(&format!("{:?}", release.date.as_str()));
        output.push_str(",\n");
        output.push_str("    },\n");
    }
    output.push_str("];\n");
    output
}

fn parse_metadata_date(value: &str, source: &str) -> Result<String, String> {
    let value = value.trim();
    if !is_iso_date(value) {
        return Err(format!(
            "R version availability metadata date in {source} must be in YYYY-MM-DD format"
        ));
    }
    Ok(value.to_string())
}

fn is_iso_date(value: &str) -> bool {
    let bytes = value.as_bytes();
    bytes.len() == 10
        && bytes[0].is_ascii_digit()
        && bytes[1].is_ascii_digit()
        && bytes[2].is_ascii_digit()
        && bytes[3].is_ascii_digit()
        && bytes[4] == b'-'
        && bytes[5].is_ascii_digit()
        && bytes[6].is_ascii_digit()
        && bytes[7] == b'-'
        && bytes[8].is_ascii_digit()
        && bytes[9].is_ascii_digit()
}
