use std::env;
use std::fs;
use std::path::PathBuf;
use std::time::{SystemTime, UNIX_EPOCH};

#[path = "src/rig/release_metadata.rs"]
mod release_metadata;

const R_VERSION_METADATA_PATH: &str = "src/rig/r-versions.json";

fn main() {
    println!("cargo:rerun-if-changed=build.rs");
    println!("cargo:rerun-if-changed=src/rig/release_metadata.rs");
    println!("cargo:rerun-if-changed={R_VERSION_METADATA_PATH}");

    let json = fs::read_to_string(R_VERSION_METADATA_PATH)
        .unwrap_or_else(|e| panic!("failed to read `{R_VERSION_METADATA_PATH}`: {e}"));
    let releases = release_metadata::parse_minor_releases_json(&json, R_VERSION_METADATA_PATH)
        .unwrap_or_else(|e| panic!("{e}"));
    let metadata_date = current_utc_date();
    let out_dir = PathBuf::from(env::var_os("OUT_DIR").expect("OUT_DIR is set by Cargo"));
    fs::write(
        out_dir.join("r_version_minor_releases.rs"),
        generated_minor_releases(&releases, &metadata_date),
    )
    .expect("failed to write generated R minor release metadata");
}

fn generated_minor_releases(
    releases: &[release_metadata::MinorRelease<'_>],
    metadata_date: &str,
) -> String {
    let mut output = String::from("// @generated by build.rs\n");
    output.push_str("pub(crate) const EMBEDDED_AVAILABLE_METADATA_DATE: &str = ");
    output.push_str(&format!("{metadata_date:?};\n"));
    output.push_str("pub(crate) const EMBEDDED_MINOR_RELEASES: &[MinorRelease<'static>] = &[\n");
    for release in releases {
        output.push_str("    MinorRelease {\n");
        output.push_str("        version: std::borrow::Cow::Borrowed(");
        output.push_str(&format!("{:?}", release.version.as_ref()));
        output.push_str("),\n");
        output.push_str("        date: std::borrow::Cow::Borrowed(");
        output.push_str(&format!("{:?}", release.date.as_ref()));
        output.push_str("),\n");
        output.push_str("    },\n");
    }
    output.push_str("];\n");
    output
}

fn current_utc_date() -> String {
    let days = SystemTime::now()
        .duration_since(UNIX_EPOCH)
        .expect("system clock is before the Unix epoch")
        .as_secs()
        / 86_400;
    let (year, month, day) = civil_from_days(days as i64);
    format!("{year:04}-{month:02}-{day:02}")
}

fn civil_from_days(days: i64) -> (i64, u32, u32) {
    let days = days + 719_468;
    let era = if days >= 0 { days } else { days - 146_096 } / 146_097;
    let day_of_era = days - era * 146_097;
    let year_of_era =
        (day_of_era - day_of_era / 1_460 + day_of_era / 36_524 - day_of_era / 146_096) / 365;
    let year = year_of_era + era * 400;
    let day_of_year = day_of_era - (365 * year_of_era + year_of_era / 4 - year_of_era / 100);
    let month_part = (5 * day_of_year + 2) / 153;
    let day = day_of_year - (153 * month_part + 2) / 5 + 1;
    let month = month_part + if month_part < 10 { 3 } else { -9 };
    let year = year + if month <= 2 { 1 } else { 0 };

    (year, month as u32, day as u32)
}
